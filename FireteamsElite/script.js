function Home() {
    document.getElementById('Home').style.display="block"
    document.getElementById('PathogenDLC').style.display="none"
    document.getElementById('Steam').style.display="none"
    document.getElementById('Activities').style.display="none"
}

function PathogenDLC() {
    document.getElementById('Home').style.display="none"
    document.getElementById('PathogenDLC').style.display="block"
    document.getElementById('Steam').style.display="none"
    document.getElementById('Activities').style.display="none"
}

function Steam() {
    document.getElementById('Home').style.display="none"
    document.getElementById('PathogenDLC').style.display="none"
    document.getElementById('Steam').style.display="block"
    document.getElementById('Activities').style.display="none"
}

function Activities() {
    document.getElementById('Home').style.display="none"
    document.getElementById('PathogenDLC').style.display="none"
    document.getElementById('Steam').style.display="none"
    document.getElementById('Activities').style.display="block"
}